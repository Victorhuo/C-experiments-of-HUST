#include<stdio.h>
int main()
{
	int t,y,i,j;
	int ly[12]={31,29,31,30,31,30,31,31,30,31,30,31}; 
	int	cy[12]={31,28,31,30,31,30,31,31,30,31,30,31};//定义两个数组储存闰年和平年的各月天数,ly表示闰年，cy为平年 
	while(scanf("%d %d",&y,&t)!=EOF){
		if((y%100!=0&&y%4!=0)||(y%100==0&&y%400!=0)){//判断是否位闰年 
			for(i=0;t>cy[i];i++){
				t-=cy[i];
			}
		}
		else{
			for(i=0;t>ly[i];i++){
				t-=ly[i];
			}	
		}
		printf("%d %d\n",++i,t); //因为i中数值位数组下标，所以转换位月份应该加1，用'++i' 
	}
	return 0;
}
